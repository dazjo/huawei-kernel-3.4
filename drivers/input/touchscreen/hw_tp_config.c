/* if ic name fail, user default configurate */
uint32_t get_tp_version_config(int moudle_id, u16 ic_number, uint8_t ** temp_version)
{   
	struct config_tp * temp_config = NULL;
	/*parameter judge*/
	if ((ic_number != ic_type_2202) && (ic_number != ic_type_3200))
	{
		printk(KERN_ERR "[TP] TOUCH_ERR: get_ic_name fail \n");
		goto err_get_ic_name_failed;
	}

    
	temp_config = get_machine_config();

	while ((temp_config != NULL))
	{
		if ((temp_config->tp_model == moudle_id)
		    && (temp_config->ic_number == ic_number))
    			break;
    		temp_config++;
	}

	if (temp_config != NULL)
	{
		*temp_version = temp_config->config;
		return temp_config->version;
	}


err_get_ic_name_failed:
        
		*temp_version = tp_default_config;
		return CURRENT_PR_VERSION;

}

